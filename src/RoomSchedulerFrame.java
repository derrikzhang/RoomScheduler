    private void checkFacultyStatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkFacultyStatusButtonActionPerformed
        String faculty = checkFacultyStatusFacultyComboBox.getSelectedItem().toString();
        String text = "";
        ArrayList<ReservationEntry>reservationList = ReservationQueries.getReservationsByFaculty(faculty);
        ArrayList<WaitlistEntry>waitList = WaitlistQueries.getWaitlistByFaculty(faculty);
        if ((reservationList.isEmpty())&&(waitList.isEmpty()))
            reservationStatusTextArea.setText("No reservation");
        else if (waitList.isEmpty()){
            text += "Reservations: \nDate \t Room\n";
            for(ReservationEntry reservation: ReservationQueries.getReservationsByFaculty(faculty)){
                text += reservation.getDate() + " \t " + reservation.getRoom() + "\n";
            }
        }
        else if (reservationList.isEmpty()){
            text += "Waitlist: \nDate\t Seats\n";
            for(WaitlistEntry waitlist: waitList){
                text += waitlist.getDate() + " \t " + waitlist.getSeats() + "\n";
            }
        }
        else{
            text += "Reservations: \nDate \t Room\n";
            for(ReservationEntry reservation: ReservationQueries.getReservationsByFaculty(faculty)){
                text += reservation.getDate() +"\t" +  reservation.getRoom() + "\n";
            }
            text += "\nWaitlist: \nDate\t Seats\n";
            for(WaitlistEntry waitlist: waitList){
                text += waitlist.getDate() + "\t" + waitlist.getSeats() + "\n";
            }
        }
        facultyStatusTextArea.setText(text);
    }//GEN-LAST:event_checkFacultyStatusButtonActionPerformed

