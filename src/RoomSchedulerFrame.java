    private void cancelReservationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelReservationButtonActionPerformed
        String faculty = cancelReservationFacultyComboBox.getSelectedItem().toString();
        Date date = (Date) cancelReservationDateComboBox.getSelectedItem();
        ArrayList<ReservationEntry>reservationList = ReservationQueries.getReservationsByFaculty(faculty);
        ArrayList<WaitlistEntry> waitList = WaitlistQueries.getWaitlistByFaculty(faculty);
        String result ="";
        if((reservationList.isEmpty())&&(waitList.isEmpty()))
            cancelReservationStatusLabel.setText("Reservation doesn't exist.");
        else if(waitList.isEmpty())
            for(ReservationEntry reservation: reservationList){
                if(reservation.getDate().equals(date))
                    result = ReservationQueries.deleteReservationEntry(reservation);
                    cancelReservationStatusLabel.setText("Reservation: " + reservation.getFaculty() + " " + reservation.getRoom()+" "+reservation.getDate()+" has been removed. " + result);
                    break;
            }
        else if(reservationList.isEmpty())
            for(WaitlistEntry waitlist: waitList){
                if(waitlist.getDate().equals(date))
                    WaitlistQueries.deleteWaitlistEntry(waitlist);
                    cancelReservationStatusLabel.setText("Reservation: " + waitlist.getFaculty() + " " + waitlist.getSeats()+" "+waitlist.getDate()+" has been removed from waitlist. "+result);
                    break;
            }
        
            
    }//GEN-LAST:event_cancelReservationButtonActionPerformed

